    private void usersTableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usersTableMouseReleased
        // TODO add your handling code here:
        int row = usersTable.getSelectedRow();
        
        String userId = tableModel.getValueAt(row, 0).toString();
        String userName = tableModel.getValueAt(row, 1).toString();
        String password = tableModel.getValueAt(row, 2).toString();
        String role = tableModel.getValueAt(row, 3).toString();
        String status = tableModel.getValueAt(row, 4).toString();
        
        userIdField.setText(userId);
        usernameField.setText(userName);
        passwordField.setText(password);
        roleField.setSelectedItem(role);
        roleField.setEnabled(false);
        statusField.setSelectedItem(status);
        
        addBtn.setEnabled(false);
    }//GEN-LAST:event_usersTableMouseReleased

