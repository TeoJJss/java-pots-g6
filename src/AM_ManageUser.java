    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        String userId = userIdField.getText();
        
        try{
            if(userId.strip().isEmpty()){
                throw new Exception("Please select a row");
            }
            User targetUser = User.getUserById(userId);
            targetUser.deleteUser();
            JOptionPane.showMessageDialog(this, "Deleted user "+ userId);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Fail to delete user: \n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        clearFields();
        setTable();
    }//GEN-LAST:event_deleteBtnActionPerformed

