    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        String userId = userIdField.getText();
        String userName = usernameField.getText().replace(",", "");
        String password = passwordField.getText().replace(",", "");
        
        try{
            if (userId.strip().isEmpty() || userName.strip().isEmpty() || password.strip().isEmpty()){
                throw new Exception("Please select a row");
            }
            User targetUser = User.getUserById(userId);
            targetUser.editUser(userName, password);
            JOptionPane.showMessageDialog(this, "Editted user "+ userId);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Fail to edit user: \n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        clearFields();
        setTable();
    }//GEN-LAST:event_editBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        String userName = usernameField.getText().replace(",", "");
        String password = passwordField.getText().replace(",", "");
        String userRole = roleField.getSelectedItem().toString();
        try{
            if(userName.strip().isEmpty() || password.strip().isEmpty() || userRole.strip().isEmpty()){
                throw new Exception("Please enter username, password and role");
             }

             User targetUser = new User(userName, password, userRole);
             targetUser.addUser();
             clearFields();
             setTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Fail to add user: \n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_addBtnActionPerformed

